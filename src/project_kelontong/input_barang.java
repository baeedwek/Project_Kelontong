    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabelBarang.getModel();
        int row = tabelBarang.getSelectedRow();
        if (row >= 0) {
        // Mendapatkan ID atau kunci unik (misalnya kolom ID_Barang) dari tabel yang akan dihapus
        String idBarang = tabelBarang.getValueAt(row, 0).toString();
        model.removeRow(row);
        try {
            
            sql = "DELETE FROM barang WHERE ID_Barang = ?";
            PreparedStatement preparedStatement = Con.prepareStatement(sql);
            preparedStatement.setString(1, idBarang);
            preparedStatement.executeUpdate();
            preparedStatement.close();
        } catch (SQLException e) {
            System.err.println("Error: " + e.getMessage());
            e.printStackTrace();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus.");
        }

    }//GEN-LAST:event_btnHapusActionPerformed

