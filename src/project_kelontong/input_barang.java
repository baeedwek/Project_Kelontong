        private void autonumber(){
        try {
            
            St = Con.createStatement();
            sql = "SELECT * FROM barang ORDER BY ID_Barang DESC";
            Rs = St.executeQuery(sql);
            if (Rs.next()) {
                String IdBarang = Rs.getString("ID_Barang").substring(3);
                String TR = "" +(Integer.parseInt(IdBarang)+1);
                String Nol = "";
                
                if(TR.length()==1)
                {Nol = "000";}
                else if(TR.length()==2)
                {Nol = "00";}
                else if(TR.length()==3)
                {Nol = "0";}
                else if(TR.length()==4)
                {Nol = "";}
                txtId.setText("BAR" + Nol + TR);
            } else {
                txtId.setText("BAR0001");
            }
            Rs.close();
            St.close();
        } catch (Exception e) {
            System.out.println("autonumber error");
        }
    }
    private void tabelBarang(){
        DefaultTableModel tb= new DefaultTableModel();
        // Memberi nama pada setiap kolom tabel
        tb.addColumn("Kode Barang");
        tb.addColumn("Nama Barang");
        tb.addColumn("Stok");
        tb.addColumn("Harga");
        tb.addColumn("Tanggal Masuk");
        tabelBarang.setModel(tb);
        try{
            // Mengambil data dari database
            St = Con.createStatement();
            Rs=St.executeQuery("select * from barang");

            while (Rs.next())
            {
            // Mengambil data dari database berdasarkan nama kolom pada tabel
            // Lalu di tampilkan ke dalam JTable
            tb.addRow(new Object[]{
            Rs.getString("ID_Barang"),
            Rs.getString("nama"),
            Rs.getString("stok"),
            Rs.getString("harga"),
            Rs.getString("tanggal_masuk"),
            });
            }

        }catch (Exception e){
        }
    }
